package com.JavaOperators;

public class RelationalOperators {

    public static void main(String[] args) {

        int age = 17;

        System.out.println("Age == 18 : " + (age == 18));
        System.out.println("Age != 18 : " + (age != 18));
        System.out.println("Age > 18 : " + (age > 18));
        System.out.println("Age < 18 : " + (age < 18));
        System.out.println("Age >= 18 : " + (age >= 18));
        System.out.println("Age <= 18 : " + (age <= 18));
    }
}